/**
 * Type coercion happens when JS try converting type of the value in some cases
 * coercion - string, number, boolean
 */

console.log('5' + 4); // 54 - convert to string
console.log('5' - 4); // 1 - convert to number
console.log(4 === 4); // true - convert to boolean
console.log(5 === 4); // false - convert to boolean